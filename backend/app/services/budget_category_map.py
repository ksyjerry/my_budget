def get_category(budget_unit: str) -> str:
    """budget_unit 문자열에서 budget_category를 결정."""
    if not budget_unit:
        return ""
    for keyword, category in _RULES:
        if budget_unit.startswith(keyword) or keyword in budget_unit:
            return category
    return "기타"
